    private void LoadData() {
        try {

            MonHocSQL monHocSQL = (MonHocSQL) Class.forName("MonHoc.MonHocSQL").newInstance();
            list = new MonHocSQL().getAll();
            for (MonHoc mh : list) {
                dtm.addRow(toObjectsData(mh));
            }
            tbMonHoc.setModel(dtm);
        } catch (Exception ex) {
            ex.toString();
        }
    }

    public static Object[] toObjectsData(MonHoc hoc) {
        Object[] objectsData = {hoc.getMaMH(), hoc.getTenMH(), hoc.getSoTC(), hoc.getHinhThucThi(), hoc.getHocKy(), hoc.getPhongHoc()};
        return objectsData;
    }

