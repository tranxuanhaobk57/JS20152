    public void reset() {
        txtMamonhoc.setText("");
        txtTenmonhoc.setText("");
        txtPhonghoc.setText("");
        txtSotinchi.setText("");
        txHocKy.setText("");
        txtMamonhoc.requestFocus();
    }

    public boolean checkInformation() {
        Check c = new Check();
        if (!c.checkID(txtMamonhoc.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập mã sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtMamonhoc.setText("");
            txtMamonhoc.requestFocus();
            return false;
        } else if (!c.checkSpace(txtTenmonhoc.getText()) || !c.check(txtTenmonhoc.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập tên môn học sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtTenmonhoc.setText("");
            txtTenmonhoc.requestFocus();
            return false;
        } else if (!c.checkSpace(txtSotinchi.getText()) || !c.checkNumber(txtSotinchi.getText())) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại số trình", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtSotinchi.setText("");
            txtSotinchi.requestFocus();
            return false;
        } else if (!c.checkSpace(txHocKy.getText()) || !c.checkNumber(txHocKy.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập sai học kỳ phải là số", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txHocKy.setText("");
            txHocKy.requestFocus();
            return false;
        } else if (!c.checkSpace(txtPhonghoc.getText()) || !c.check(txtPhonghoc.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập tên phòng học sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txtPhonghoc.setText("");
            txtPhonghoc.requestFocus();
            return false;

        }
        return true;
    }

    private void Display() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        ArrayList<MonHoc> hocs = new MonHocSQL().getAll();
        for (MonHoc mh : hocs) {
            Vector v = new Vector();
            v.add(mh.getMaMH());
            v.add(mh.getTenMH());
            v.add(mh.getSoTC());
            v.add(mh.getHinhThucThi());
            v.add(mh.getHocKy());
            v.add(mh.getPhongHoc());

            dtm.addRow(v);
        }
        reset();
        // jtfthi.setText("");
    }
