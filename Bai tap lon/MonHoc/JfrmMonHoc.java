    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {
        String maMH = txtMamonhoc.getText();
        String tenMH = txtTenmonhoc.getText();
        String soTrinh = txtSotinchi.getText();
        String htThi = (String) cbHinhthucthi.getSelectedItem();
        String hocKy = txHocKy.getText();
        String phongHoc = txtPhonghoc.getText();
        Check c = new Check();
        if (!checkInformation()) {
            return;
        }

        int strinh;
        strinh = Integer.parseInt(soTrinh);
        int hky;
        hky = Integer.parseInt(hocKy);

         MonHoc monHoc = new MonHoc(maMH, tenMH,strinh, phongHoc,hky ,htThi);
        ArrayList<MonHoc> listCheck = new MonHocSQL().CheckID(maMH);
        if (listCheck.size() > 0) {
            JOptionPane.showMessageDialog(this, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
            txtMamonhoc.setText("");
            txtMamonhoc.requestFocus();
        } else {
            MonHoc insert = new MonHocSQL().addNew(monHoc);
            if (insert != null) {
                Display();
            }
        }

    }

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {
        String maMH = txtMamonhoc.getText();
        String tenMH = txtTenmonhoc.getText();
        String soTrinh = txtSotinchi.getText();
        String htThi = (String) cbHinhthucthi.getSelectedItem();
        String hocKy = txHocKy.getText();
        String phongHoc = txtPhonghoc.getText();
        Check c = new Check();
        if (!checkInformation()) {
            return;
        }

        int strinh;
        strinh = Integer.parseInt(soTrinh);
        int hky;
        hky = Integer.parseInt(hocKy);
        MonHoc monHoc = new MonHoc(maMH, tenMH,strinh, phongHoc,hky ,htThi);
        MonHoc update = new MonHocSQL().update(monHoc);
        if (update != null) {
            Display();
        }
        btnNew.setEnabled(true);
        btnUpdate.setEnabled(false);
        btnDelete.setEnabled(false);
        txtMamonhoc.setEnabled(true);
    }

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {
        String maMh = txtMamonhoc.getText();
        int b = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông Báo", JOptionPane.YES_NO_OPTION);
        if (b == JOptionPane.YES_OPTION) {
            try {
                new MonHocSQL().delMH(maMh);
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu chưa thể xóa, có tồn tại điểm của môn này, hãy xóa dữ liệu điểm trước", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }
            reset();
            LoadData();
            btnNew.setEnabled(true);
            btnUpdate.setEnabled(false);
            btnDelete.setEnabled(false);
            txtMamonhoc.setEnabled(true);
        }
    }

