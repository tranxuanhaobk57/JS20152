    public void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        ArrayList<BangDiem> bangDiems = new BangDiemSQL().getAll();
        for (BangDiem bd : bangDiems) {
            Vector v = new Vector();
            v.add(bd.getMaSV());
            v.add(bd.getMaHocPhan());
            v.add(bd.getLanThi());
            v.add(bd.getHeSo());
            v.add(bd.getDiem());
            if (bd.isTrangThai() == true) {
                v.add("Bật");
            } else {
                v.add("Tắt");

            }
            dtm.addRow(v);
        }
        reset();
    }

