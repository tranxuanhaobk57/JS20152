    public boolean checkinfo() {
        Check c = new Check();
        if (!c.checkSpace(txMark.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập điểm sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            txMark.setText("");
            txMark.requestFocus();
            return false;
        }
        return true;
    }

